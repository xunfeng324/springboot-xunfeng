package xunfemg.controller;

public class Java8_defaultfun {
    public static void main(String[] args) {
        //Java 8 默认方法
        defaultfun();
    }
    private static void defaultfun() {
        /*
         * 默认方法就是接口可以有实现方法，而且不需要实现类去实现其方法。
         * 我们只需在方法名前面加个 default 关键字即可实现默认方法。
         * */
    }
}
